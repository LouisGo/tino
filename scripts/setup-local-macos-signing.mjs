import crypto from "node:crypto";
import {
  existsSync,
  mkdirSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import os from "node:os";
import path from "node:path";
import { spawnSync } from "node:child_process";

if (process.platform !== "darwin") {
  console.error("[setup-local-macos-signing] This script only runs on macOS.");
  process.exit(1);
}

const projectRoot = path.resolve(import.meta.dirname, "..");
const repoLocalDir = path.join(projectRoot, ".local");
const stateDir = path.join(repoLocalDir, "local-codesign");
const globalConfigDir = path.join(os.homedir(), ".config", "tino");
const keychainPath = path.join(os.homedir(), "Library", "Keychains", "TinoLocalCodeSigning.keychain-db");
const envPath = path.join(repoLocalDir, "macos-signing.env");
const globalEnvPath = path.join(globalConfigDir, "macos-signing.env");
const commonName = "Tino Local Code Signing";
const keychainPassword = crypto.randomBytes(24).toString("base64url");
const p12Password = crypto.randomBytes(24).toString("base64url");
const opensslConfigPath = path.join(stateDir, "openssl.cnf");
const privateKeyPath = path.join(stateDir, "tino-local-codesign.key");
const certificatePath = path.join(stateDir, "tino-local-codesign.crt");
const pkcs12Path = path.join(stateDir, "tino-local-codesign.p12");

function run(bin, args, options = {}) {
  const result = spawnSync(bin, args, {
    encoding: "utf8",
    stdio: "pipe",
    ...options,
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    const output = `${result.stdout ?? ""}\n${result.stderr ?? ""}`
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean)[0] ?? `${bin} exited with code ${result.status ?? 1}`;
    throw new Error(output);
  }

  return result;
}

function parseKeychainList(output) {
  return output
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => line.replace(/^"|"$/g, ""));
}

function ensureDir(dirPath) {
  mkdirSync(dirPath, { recursive: true });
}

ensureDir(stateDir);

const opensslConfig = `
[ req ]
default_bits = 2048
prompt = no
default_md = sha256
distinguished_name = dn
x509_extensions = codesign

[ dn ]
CN = ${commonName}

[ codesign ]
basicConstraints = critical,CA:TRUE
keyUsage = critical,digitalSignature,keyCertSign,cRLSign
extendedKeyUsage = critical,codeSigning
subjectKeyIdentifier = hash
authorityKeyIdentifier = keyid,issuer
`.trimStart();

writeFileSync(opensslConfigPath, opensslConfig, "utf8");

run("openssl", [
  "req",
  "-x509",
  "-newkey",
  "rsa:2048",
  "-keyout",
  privateKeyPath,
  "-out",
  certificatePath,
  "-days",
  "3650",
  "-nodes",
  "-config",
  opensslConfigPath,
]);

run("openssl", [
  "pkcs12",
  "-legacy",
  "-export",
  "-out",
  pkcs12Path,
  "-inkey",
  privateKeyPath,
  "-in",
  certificatePath,
  "-certpbe",
  "PBE-SHA1-3DES",
  "-keypbe",
  "PBE-SHA1-3DES",
  "-macalg",
  "sha1",
  "-name",
  commonName,
  "-passout",
  `pass:${p12Password}`,
]);

if (existsSync(keychainPath)) {
  rmSync(keychainPath, { force: true });
}

run("security", ["create-keychain", "-p", keychainPassword, keychainPath]);
run("security", ["set-keychain-settings", "-lut", "21600", keychainPath]);
run("security", ["unlock-keychain", "-p", keychainPassword, keychainPath]);
run("security", [
  "import",
  pkcs12Path,
  "-k",
  keychainPath,
  "-P",
  p12Password,
  "-T",
  "/usr/bin/codesign",
  "-T",
  "/usr/bin/security",
]);
run("security", [
  "add-trusted-cert",
  "-d",
  "-r",
  "trustRoot",
  "-p",
  "codeSign",
  "-k",
  keychainPath,
  certificatePath,
]);
run("security", [
  "set-key-partition-list",
  "-S",
  "apple-tool:,apple:",
  "-s",
  "-k",
  keychainPassword,
  keychainPath,
]);

const existingKeychains = parseKeychainList(
  run("security", ["list-keychains", "-d", "user"]).stdout,
);
const desiredKeychains = Array.from(new Set([keychainPath, ...existingKeychains]));
run("security", ["list-keychains", "-d", "user", "-s", ...desiredKeychains]);

const envFile = `# Generated by scripts/setup-local-macos-signing.mjs
TINO_MACOS_SIGNING_IDENTITY="${commonName}"
TINO_MACOS_SIGNING_KEYCHAIN="${keychainPath}"
TINO_MACOS_SIGNING_KEYCHAIN_PASSWORD="${keychainPassword}"
`;
ensureDir(repoLocalDir);
ensureDir(globalConfigDir);
writeFileSync(envPath, envFile, "utf8");
writeFileSync(globalEnvPath, envFile, "utf8");

const identityOutput = run("security", [
  "find-identity",
  "-v",
  "-p",
  "codesigning",
  keychainPath,
]);
const identitySummary = identityOutput.stdout.trim();
if (!identitySummary.includes(commonName) || identitySummary.includes("0 valid identities found")) {
  throw new Error(
    "local signing certificate was imported but macOS still does not recognize it as a valid code-signing identity; rerun the script or create the certificate manually in Keychain Access as a Self Signed Root / Code Signing identity",
  );
}

console.log(identitySummary);
console.log(`[setup-local-macos-signing] Wrote ${envPath}`);
console.log(`[setup-local-macos-signing] Wrote ${globalEnvPath}`);
console.log("[setup-local-macos-signing] Rebuild with `pnpm tauri:build-install:test`.");
